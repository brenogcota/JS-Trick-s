// Retirar espaços em branco com método trim()

"   some whitespace string  ".trim();
"   some whitespace string  ".trimStart(); // "some whitespace string  "
"   some whitespace string  ".trimEnd();   //  "   some whitespace string"
"   some whitespace string  ".trimLeft();  // "some whitespace string  "
"   some whitespace string  ".trimRight(); //  "   some whitespace string"